import {
	Address,
	COMPILOT_CHAINS,
} from "@compilot/sig-gating-contracts-sdk/lib";

// This function returns an address for a MultiSig if has been deployed on the input network
export const getMultiSigAddress = (chainId: string) => {
	switch (chainId) {
		case COMPILOT_CHAINS.SEPOLIA:
			return SAFE_ADDRESS[COMPILOT_CHAINS.SEPOLIA];
		case COMPILOT_CHAINS.POLYGON:
			return SAFE_ADDRESS[COMPILOT_CHAINS.POLYGON];
		case COMPILOT_CHAINS.BASE:
			return SAFE_ADDRESS[COMPILOT_CHAINS.BASE];
		case COMPILOT_CHAINS.ETHEREUM:
			return SAFE_ADDRESS[COMPILOT_CHAINS.ETHEREUM];
		case COMPILOT_CHAINS.ARBITRUM:
			return SAFE_ADDRESS[COMPILOT_CHAINS.ARBITRUM];
		case COMPILOT_CHAINS.BNB:
			return SAFE_ADDRESS[COMPILOT_CHAINS.BNB];
		case COMPILOT_CHAINS.OPTIMISM:
			return SAFE_ADDRESS[COMPILOT_CHAINS.OPTIMISM];
		case COMPILOT_CHAINS.AVALANCHE:
			return SAFE_ADDRESS[COMPILOT_CHAINS.AVALANCHE];
		default:
			console.log(
				"--- WARNING --- No SAFE_ADDRESS specified for this Chain, using default Signer Manager Controller",
			);
			return undefined;
	}
};

